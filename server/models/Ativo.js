class Ativo {
    constructor(id, nome, codInterno, descricao, valor, status, dataAquisicao, qtdReparos) {
      this.id = id;
      this.nome = nome;
      this.codInterno = codInterno;
      this.descricao = descricao;
      this.valor = valor;
      this.status = status;
      this.dataAquisicao = dataAquisicao;
      this.qtdReparos = qtdReparos;
    }
  }
  
  export default Ativo;
  